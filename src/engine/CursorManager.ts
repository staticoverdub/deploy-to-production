  private makeCursor(key: string, draw: (ctx: CanvasRenderingContext2D) => void): void {
    const canvas = this.scene.textures.createCanvas(key, 16, 16);
    if (!canvas) return;
    const ctx = canvas.getContext();
    draw(ctx);
    canvas.refresh();
  }

  private col(ctx: CanvasRenderingContext2D, color: string, pixels: number[][]): void {
    ctx.fillStyle = color;
    for (const [x, y] of pixels) {
      ctx.fillRect(x, y, 1, 1);
    }
  }
